let currentLang = localStorage.getItem('language') || 'en';

function applyTranslations() {
  const elements = document.querySelectorAll('[data-i18n]');
  
  elements.forEach(el => {
    const keyString = el.getAttribute('data-i18n');
    const keys = keyString.split('.');
    let value = translations[currentLang];
    
    // Traverse the translations object using the key string
    for (let key of keys) {
      if (value) {
        value = value[key];
      }
    }
    
    if (value) {
      // Check if element has an SVG icon inside (like buttons)
      const svg = el.querySelector('svg');
      if (svg) {
        // We only want to replace the text node, not the SVG
        Array.from(el.childNodes).forEach(child => {
          if (child.nodeType === Node.TEXT_NODE && child.textContent.trim().length > 0) {
            child.textContent = value + ' ';
          }
        });
      } else {
         if(value.includes('<strong>') || value.includes('<br>')) {
           el.innerHTML = value;
         } else {
           el.textContent = value;
         }
      }
    }
  });

  document.documentElement.setAttribute('lang', currentLang);
  
  const langBtns = document.querySelectorAll('.lang-toggle');
  langBtns.forEach(btn => {
    btn.textContent = currentLang === 'en' ? 'EN' : 'VN';
  });
}

function initLanguageToggle() {
  const langBtns = document.querySelectorAll('.lang-toggle');
  
  langBtns.forEach(btn => {
    btn.addEventListener('click', () => {
      currentLang = currentLang === 'en' ? 'vn' : 'en';
      localStorage.setItem('language', currentLang);
      applyTranslations();
    });
  });
}

function init() {
  initLanguageToggle();
  applyTranslations();
}

if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', init);
} else {
  init();
}
